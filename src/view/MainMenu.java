    private void setDateAndTime() {
        clockTimer = new Timer(1000, e -> {
            SimpleDateFormat sdf
                    = new SimpleDateFormat("yyyy-MM-dd | EEEE | hh:mm:ss a", Locale.getDefault());
            String currentDateAndTime = sdf.format(new Date());
            lblDateAndTime.setText(currentDateAndTime);
        });
        clockTimer.start();
    }

