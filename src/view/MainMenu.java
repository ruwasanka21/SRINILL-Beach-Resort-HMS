    private void updateLoginDetail(String username) {
        // SQL query to update logout timestamp for the latest login of the user
        String query = "UPDATE login_detail "
                + "SET login_detail_logout_date_time = CURRENT_TIMESTAMP "
                + "WHERE login_detail_username = ? "
                + "ORDER BY login_detail_id DESC LIMIT 1";

        try (Connection con = DatabaseLayer.getConnection();
                PreparedStatement pst = con.prepareStatement(query)) {

            pst.setString(1, username);  // set username
            pst.executeUpdate();  // execute update

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    private void loadMonthsToCombo() {
        comboMonth.removeAllItems();

        String[] months = {
            "January", "February", "March", "April", "May", "June",
            "July", "August", "September", "October", "November", "December"
        };

        for (String month : months) {
            comboMonth.addItem(month);
        }

        int currentMonthIndex = java.util.Calendar.getInstance().get(java.util.Calendar.MONTH);
        comboMonth.setSelectedIndex(currentMonthIndex);
    }

