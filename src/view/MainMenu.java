    private void webButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_webButton5ActionPerformed
        try {
            Map<String, Object> params = new HashMap<>();
            int selectedMonth = comboMonth.getSelectedIndex() + 1;
            int currentYear = java.time.LocalDate.now().getYear();

            LocalDate firstDate = LocalDate.of(currentYear, selectedMonth, 1);
            LocalDate lastDate = firstDate.with(java.time.temporal.TemporalAdjusters.lastDayOfMonth());

            params.put("fromDate", java.sql.Date.valueOf(firstDate));
            params.put("toDate", java.sql.Date.valueOf(lastDate));
            params.put("month", currentYear + " / " + comboMonth.getSelectedItem());
            CommonControl.printReport("monthly_payments", params, PrintStatus.WITH_VIEW);
        } catch (JRException ex) {
            Logger.getLogger(MainMenu.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(MainMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_webButton5ActionPerformed

