    private void btnFilterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFilterActionPerformed
        // TODO add your handling code here:
        String empID = txtFilterEmpID.getText().trim(); 

    if (empID.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please enter an Employee ID to filter.");
        return;
    }

    try {
        Connection con = DatabaseLayer.mycon(); 
        String query = "SELECT task_id, employee_id, task_description, assigned_date, dutyDate, dutyTime FROM employeetasks WHERE employee_id = ?";
        PreparedStatement pst = con.prepareStatement(query);
        pst.setString(1, empID);

        ResultSet rs = pst.executeQuery();

        // Clear table before loading new data
        DefaultTableModel model = (DefaultTableModel) empAssignTable.getModel();
        model.setRowCount(0);

        while (rs.next()) {
            Object[] row = {
                rs.getInt("task_id"),
                rs.getInt("employee_id"),
                rs.getString("task_description"),
                rs.getString("assigned_date"),
                rs.getString("dutyDate"),
                rs.getString("dutyTime")
            };
            model.addRow(row);
        }

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Database error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnFilterActionPerformed

