    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
                                         
    try {
        int taskID = Integer.parseInt(txtTaskID.getText());
        String description = txtDescription.getText();

        // Get date and time from JSpinnerDateEditor
        Date fullDate = (Date) setDate.getValue();
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat timeFormat = new SimpleDateFormat("HH:mm:ss");

        String dutyDate = dateFormat.format(fullDate);
        String dutyTime = timeFormat.format(fullDate);

        Connection con = DatabaseLayer.mycon();
        String sql = "UPDATE employeetasks SET  task_description = ?, dutyDate = ?, dutyTime = ? WHERE task_id = ?";

        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, description);
        pst.setString(2, dutyDate);
        pst.setString(3, dutyTime);
        pst.setInt(4, taskID);

        int rowsAffected = pst.executeUpdate();

        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "✅ Task updated successfully!");
            txtTaskID.setText("");
            txtEmpID.setText("");
            txtDescription.setText("");
            loadAssignedTasks();
        } else {
            JOptionPane.showMessageDialog(null, "⚠️ No record updated. Check Task ID.");
        }

    } catch (SQLException | NumberFormatException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "❌ Error: " + ex.getMessage());
    }


    }//GEN-LAST:event_btnUpdateActionPerformed

