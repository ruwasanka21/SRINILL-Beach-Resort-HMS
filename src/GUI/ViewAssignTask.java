    private void loadAssignedTasks() {
    Connection con = null;
    PreparedStatement pst = null;
    ResultSet rs = null;

    try {
        con = DatabaseLayer.mycon(); // Your custom DB connection method

        String sql = "SELECT task_id, employee_id, task_description, assigned_date, dutyDate, dutyTime FROM employeetasks ORDER BY task_id";
        pst = con.prepareStatement(sql);
        rs = pst.executeQuery();

        // Get table model from JTable
        DefaultTableModel model = (DefaultTableModel) empAssignTable.getModel();
        model.setRowCount(0); // Clear existing rows

        // Loop through ResultSet and add rows to the table model
        while (rs.next()) {
            int taskId = rs.getInt("task_id");
            int empId = rs.getInt("employee_id");
            String desc = rs.getString("task_description");
            String assignedDate = rs.getString("assigned_date");
            String dutyDate = rs.getString("dutyDate");
            String dutyTime = rs.getString("dutyTime");

            model.addRow(new Object[]{taskId, empId, desc, assignedDate, dutyDate, dutyTime});
        }

    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error loading task data: " + ex.getMessage());
    }
}
    
    private void loadSelectedRowToFields() {
    int selectedRow = empAssignTable.getSelectedRow();
    if (selectedRow >= 0) {
        DefaultTableModel model = (DefaultTableModel) empAssignTable.getModel();

        txtTaskID.setText(model.getValueAt(selectedRow, 0).toString());
        txtEmpID.setText(model.getValueAt(selectedRow, 1).toString());
        txtDescription.setText(model.getValueAt(selectedRow, 2).toString());
        
        try {
            String dutyDate = model.getValueAt(selectedRow, 4).toString(); 
            String dutyTime = model.getValueAt(selectedRow, 5).toString(); 

            // Combine into one datetime string
            String dateTimeString = dutyDate + " " + dutyTime;
            
            // Define the format
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            Date combinedDateTime = sdf.parse(dateTimeString);
            
            // Set it to the JSpinnerDateEditor
            setDate.setValue(combinedDateTime);

        } catch (ParseException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "❌ Error parsing date/time: " + ex.getMessage());
        }
    }
}

