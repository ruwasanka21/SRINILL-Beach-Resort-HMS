    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        Connection con = DatabaseLayer.mycon();
        PreparedStatement pst = null;

        String custID = txtCustId.getText();
        String name = txtCustName.getText();
        String email = txtCustEmail.getText();
        String NIC = txtNIC.getText();
        String phone = txtCustPhone.getText();
        String address = txtCustAddress.getText();
        String gender = radioButtonMale.isSelected() ? "Male" : "Female";
        

        try {
            String sql = "UPDATE customers SET name = ?, gender = ?, email = ?, NIC = ?, phone = ?, address = ? WHERE customer_id = ?";
            pst = con.prepareStatement(sql);

            pst.setString(1, name);
            pst.setString(2, gender);
            pst.setString(3, email);
            pst.setString(4, NIC);
            pst.setString(5, phone);
            pst.setString(6, address);
            pst.setString(7, custID);
            

            int rowsUpdated = pst.executeUpdate();

            // Check if the update was successful
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(this, "Customer Name: " + name + " Updated Successfully!");
                loadCustomerData();  // Refresh the data in the table after the update

                txtCustId.setText("");
                txtCustName.setText("");
                txtCustEmail.setText("");
                txtCustPhone.setText("");
                txtCustAddress.setText("");
                txtNIC.setText("");
                radioButtonMale = null;
                

            } else {
                JOptionPane.showMessageDialog(this, "Failed to Update Employee Data.");
            }

        } catch (SQLException ex) {
            Logger.getLogger(Employee.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

