    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        Connection con = DatabaseLayer.mycon();
    PreparedStatement pst = null;

    String custID = txtCustId.getText(); // Get the customer ID from the input field

    if (custID.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please select a customer to delete.");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this, 
        "Are you sure you want to delete this customer?", 
        "Confirm Deletion", JOptionPane.YES_NO_OPTION);
    
    if (confirm == JOptionPane.YES_OPTION) {
        try {
            String sql = "DELETE FROM customers WHERE customer_id = ?";
            pst = con.prepareStatement(sql);
            pst.setString(1, custID);

            int rowsDeleted = pst.executeUpdate();

            if (rowsDeleted > 0) {
                JOptionPane.showMessageDialog(this, "Customer Deleted Successfully!");
                loadCustomerData();  // Refresh the data in the table

                // Clear the input fields
                txtCustId.setText("");
                txtCustName.setText("");
                txtCustEmail.setText("");
                txtCustPhone.setText("");
                txtCustAddress.setText("");
                radioButtonMale=null; // Unselect radio buttons
            } else {
                JOptionPane.showMessageDialog(this, "Failed to Delete Customer.");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database Error: " + ex.getMessage());
        }
    }
        
    }//GEN-LAST:event_btnDeleteActionPerformed


