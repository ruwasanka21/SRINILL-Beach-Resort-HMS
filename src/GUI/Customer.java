    private void loadCustomerData() {
    Connection con = DatabaseLayer.mycon();
    PreparedStatement pst = null;
    ResultSet rs = null;
    
    try {
        String sql = "SELECT * FROM customers";
        pst = con.prepareStatement(sql);
        rs = pst.executeQuery();

        DefaultTableModel model = (DefaultTableModel) customerTable.getModel();
        model.setRowCount(0); // Clear existing rows before adding new data

        while (rs.next()) {
            Object[] row = {
                rs.getInt("customer_id"), 
                rs.getString("name"), 
                rs.getString("gender"), 
                rs.getString("email"),
                rs.getString("NIC"),
                rs.getString("phone"), 
                rs.getString("address"), 
                rs.getString("created_at"), 
                     };
            model.addRow(row); // Add row to JTable
        }

    } catch (SQLException ex) {
        Logger.getLogger(this.getClass().getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "Error loading customer data!");
    } finally {
        try {
            if (rs != null) rs.close();
            if (pst != null) pst.close();
            if (con != null) con.close();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
}
    
    private void addTableClickListener() {
        customerTable.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent e) {
                int row = customerTable.getSelectedRow();
                if (row != -1) {
                    // Get data from the selected row
                    String customerID = customerTable.getValueAt(row, 0).toString();
                    String name = customerTable.getValueAt(row, 1).toString();
                    String gender = customerTable.getValueAt(row, 2).toString();
                    String email = customerTable.getValueAt(row, 3).toString();
                    String NIC = customerTable.getValueAt(row, 4).toString();
                    String phone = customerTable.getValueAt(row, 5).toString();
                    String address = customerTable.getValueAt(row, 6).toString();
                    
                    
                    // Set values in the text fields
                    txtCustId.setText(customerID);
                    txtCustName.setText(name);
                    txtCustEmail.setText(email);
                    txtNIC.setText(NIC);
                    txtCustPhone.setText(phone);
                    txtCustAddress.setText(address);
                    
                    // Set gender (assuming radio buttons for Male/Female)
                    if (gender.equalsIgnoreCase("Male")) {
                        radioButtonMale.setSelected(true);
                    } else {
                        radioButtonFemale.setSelected(true);
                    }
                  
                }
            }
        });
    }



